public class exp3 {
    public static void main(String[] args) {
        System.out.println("Simple Math Operations");
        
        // Math
        int a = 10;
        int b = 5;
        
        System.out.println("10 + 5 = " + (a + b));
        System.out.println("10 - 5 = " + (a - b));
        System.out.println("10 * 5 = " + (a * b));
        System.out.println("10 / 5 = " + (a / b));
        
        // Compare
        System.out.println("10 > 5 = " + (a > b));
        System.out.println("10 < 5 = " + (a < b));
        System.out.println("10 == 5 = " + (a == b));
        
        // True/False
        boolean sunny = true;
        boolean weekend = false;
        
        System.out.println("Sunny AND Weekend = " + (sunny && weekend));
        System.out.println("Sunny OR Weekend = " + (sunny || weekend));
        
        System.out.println("Done!");
    }
}
